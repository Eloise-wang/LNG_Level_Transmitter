/* 发送单字节 OpCode 命令 */
static void PCAP01_SendOpCode(uint8_t cmd)
{
    uint8_t rxDummy;
    (void)LPSPI_DRV_MasterTransferBlocking(INST_LPSPI_0, &cmd, &rxDummy, 1U, SPI_TIMEOUT_MS);
}

/* 发送 32 位寄存器写命令 (4 字节, MSB 优先) */
static void PCAP01_WriteReg(uint32_t cmd)
{
    uint8_t txBuf[4];

    txBuf[0] = (uint8_t)((cmd >> 24U) & 0xFFU);
    txBuf[1] = (uint8_t)((cmd >> 16U) & 0xFFU);
    txBuf[2] = (uint8_t)((cmd >> 8U)  & 0xFFU);
    txBuf[3] = (uint8_t)(cmd & 0xFFU);

    (void)LPSPI_DRV_MasterTransferBlocking(INST_LPSPI_0, txBuf, NULL, 4U, SPI_TIMEOUT_MS);
}

/* 发送 24 位固件写入命令 (16位SRAM地址 + 8位数据, MSB 优先) */
static void PCAP01_WriteSRAM(uint16_t addrCmd, uint8_t data)
{
    uint8_t txBuf[3];

    txBuf[0] = (uint8_t)((addrCmd >> 8U) & 0xFFU);
    txBuf[1] = (uint8_t)(addrCmd & 0xFFU);
    txBuf[2] = data;

    (void)LPSPI_DRV_MasterTransferBlocking(INST_LPSPI_0, txBuf, NULL, 3U, SPI_TIMEOUT_MS);
}

/* 下载固件到 PCAP01 SRAM */
static void PCAP01_DownloadFirmware(void)
{
    uint16_t counter;

    for (counter = 0U; counter < PCAP01_FIRMWARE_SIZE; counter++)
    {
        /* 0x9000 = SRAM 写命令前缀, 合并 16位地址 + 8位数据 → 24位单帧 */
        PCAP01_WriteSRAM((uint16_t)(counter | PCAP01_SRAM_WRITE), gs_pcap01Firmware[counter]);
    }
}

/*******************************************************************************
 * API
 ******************************************************************************/

void PCAP01_Init(void)
{
    /* 1. 初始化 LPSPI0 (使用 board 层已配置的参数) */
    LPSPI_DRV_MasterInit(INST_LPSPI_0, &lpspi_0State, &lpspi_0_MasterConfig0);

    OSIF_TimeDelay(10U);

    /* 2. 软件复位 PCAP01 */
    PCAP01_SendOpCode(PCAP01_CMD_RESET);
    OSIF_TimeDelay(2U);

    /* 3. 下载 DSP 固件到 SRAM */
    PCAP01_DownloadFirmware();

    /* 4. 写配置寄存器 (32位/寄存器, 地址编码在高4位) */
    /* Reg 0: 关闭SRAM/OTP保护, 主机模式 */
    PCAP01_WriteReg(0xC04200FFU);

    /* Reg 1: 固定值 */
    PCAP01_WriteReg(0xC1201022U);

    /* Reg 2: PC0-PC7 使能, 接地模式, 内部放电电阻 90K */
    PCAP01_WriteReg(0xC203150BU);

    /* Reg 3: CDC 周期 20us, 时钟触发模式, 200次平均 */
    PCAP01_WriteReg(0xC3062001U);

    /* Reg 4: 定时器触发模式, CDC 周期时间 */
    PCAP01_WriteReg(0xC4080510U);

    /* Reg 5: 温度平均 1 次 */
    PCAP01_WriteReg(0xC5000000U);

    /* Reg 6: 温度设置, 8次虚拟测量 */
    PCAP01_WriteReg(0xC6004140U);

    /* Reg 7: 固定值 */
    PCAP01_WriteReg(0xC71F0000U);

    /* Reg 8: DSP 内存配置 */
    PCAP01_WriteReg(0xC8800010U);

    /* Reg 9: PWM/PDM 输出配置 */
    PCAP01_WriteReg(0xC9BF208FU);

    /* Reg 10: 核心电压调节器 (0x47 = 标准功率) */
    PCAP01_WriteReg(0xCA180047U);

    /* Reg 13: C7/C0 输出选择 */
    PCAP01_WriteReg(0xCD000007U);

    /* Reg 14: 电容斜率调整 */
    PCAP01_WriteReg(0xCE002FF0U);

    /* Reg 15: 电容偏移调整 */
    PCAP01_WriteReg(0xCF000000U);

    /* 5. 芯片启动序列 */
    PCAP01_WriteReg(0xD3200000U);
    OSIF_TimeDelay(1U);

    PCAP01_WriteReg(0xD4000001U);
    OSIF_TimeDelay(1U);

    /* 6. 重置数据指针并开始转换 */
    PCAP01_SendOpCode(PCAP01_CMD_RESET_PTR);
    OSIF_TimeDelay(1U);

    PCAP01_SendOpCode(PCAP01_CMD_START_CYCLE);
}

uint32_t PCAP01_ReadData(uint32_t index)
{
    /* 0x40 + index = 读结果寄存器命令 (0x41=C0, 0x42=C1, ...) */
    uint8_t txBuf[4] = {
        (uint8_t)(PCAP01_READ_CMD | (index & 0x0FU)),
        0x00U,
        0x00U,
        0x00U
    };
    uint8_t rxBuf[4] = {0U, 0U, 0U, 0U};

    status_t status = LPSPI_DRV_MasterTransferBlocking(INST_LPSPI_0, txBuf, rxBuf, 4U, SPI_TIMEOUT_MS);
    if (status != STATUS_SUCCESS)
    {
        return 0U;
    }

    /* rxBuf[0] = 命令发送期间的无效数据, rxBuf[1:3] = 24位结果 (MSB 优先) */
    return ((uint32_t)rxBuf[1] << 16U) | ((uint32_t)rxBuf[2] << 8U) | (uint32_t)rxBuf[3];
}

uint32_t PCAP01_RawToCapacitance_pF(uint32_t rawValue)
{
    uint32_t raw24 = (uint32_t)(rawValue & PCAP01_RESULT_MASK);

    uint64_t numerator = (uint64_t)raw24 * (uint64_t)PCAP01_CREF_PF * 1000ULL;

    numerator += (1ULL << (PCAP01_Q21_SHIFT - 1U));

    return (uint32_t)(numerator >> PCAP01_Q21_SHIFT);
}
